<div id="tem"><p>5. &#191;En qu&eacute; momento debo hacer el registro&#63;</p></div>
<div id="contenidos">
<p>
Suele ser m&aacute;s conveniente justo antes, durante o despu&eacute;s de completada una actividad, para evitar depender de 
nuestra memoria. Sin embargo, cuando la naturaleza de las actividades no nos lo permitan, un registro manual puede ser 
&uacute;til para registrar luego los datos en el AST online. Los datos registrados se almacenar&aacute;n 
autom&aacute;ticamente, cada coordinador de &aacute;rea es responsable de verificar al finalizar la semana que la 
informaci&oacute;n est&eacute; completa, y que los registros de tiempo sean consistentes y confiables.<br/>

</p>
</div>
<div id="tem"><p>6. &#191;Como registro el tiempo de los proyectos&#63;</p></div>
<div id="contenidos">
<p>Los proyectos est&aacute;n clasificados como Proyectos de Negocio (IS) y Proyecto de Tecnolog&iacute;a (IT) los cuales 
est&aacute;n asignados a &aacute;reas especificas de trabajo. Al momento registrar una actividad relacionada aun proyecto, 
primero se debe seleccionar la empresa y luego el nombre del proyecto. <br/>
El significado de las siglas utilizadas para la codificaci&oacute;n de los proyectos son los siguientes:<br/><br/>
SMO - Service Management Office <br/>

SDM - Service Delivery Management <br/>

NSM - Network &amp; Security Management<br/>

AMM - Applications &amp; Systems Management<br/>

ESM - Enterprise Systems Management<br/>

IOM&nbsp; - Infrastructure Operations Management<br/>
</p>
</div>
<div id="tem"><p>7. &#191;Cu&aacute;nto tiempo tomar&aacute; registrar el tiempo&#63;</p></div>
<div id="contenidos">
<p>M&iacute;nimo. Si los miembros del equipo realizan el registro en el momento adecuado, se evitar&aacute;n
las horas extraordinarias, al inicio o final del d&iacute;a, para completar la informaci&oacute;n. La disciplina
de controlar nuestro tiempo para luego analizarlo es algo que requerir&aacute; tanto de la colaboraci&oacute;n
de los miembros del equipo como de los l&iacute;deres de las distintas &aacute;reas. En la medida que podamos
entender el uso de los recursos disponibles, podremos planificar un mejor servicio.</p>
</div>

