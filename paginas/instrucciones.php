<div id="tem"><p>1. &iquest;Por qu&eacute; debemos implantar un AST&#63;</p></div>
<div id="contenidos">
<p>SITES se ha convertido en una empresa de servicio. El tiempo es nuestro activo m&aacute;s importante y
constituye un insumo cr&iacute;tico para la facturaci&oacute;n a nuestros clientes. Para fomentar relaciones de
confianza, es importante llevar un registro que permita demostrar que la distribuci&oacute;n de los costos es
equitativa.</p>
</div>
<div id="tem"><p>2. &#191;Qu&eacute; beneficios aporta llevar un AST&#63;</p></div>
<div id="contenidos">
<p>Ayuda a relacionar la capacidad instalada con la demanda, decidir mejor la asignaci&oacute;n de los recursos,
justificar nuevas posiciones, definir estrategias en base al tipo de actividades con mayor consumo, supervisar
el adecuado balance de trabajo entre los miembros del equipo, visualizar los d&iacute;as/meses de mayor demanda para
los clientes y los que por su bajo nivel de demanda son ideales para mantenimientos internos, mejorar el esquema
de compensaciones, atender de manera m&aacute;s personalizada a los clientes internos, entre otros.</p>
</div>
<div id="tem"><p>3. &#191;Qu&eacute; actividades debo registrar&#63;</p></div>
<div id="contenidos">
<p>Todas las que se realizan durante el d&iacute;a, incluyendo interrupciones importantes que puedan desviarnos de
otras tareas que hayamos planeado. Es posible que m&aacute;s de alguna actividad no pueda anotarse justo en el momento
que ocurre, pero todos deber&iacute;amos recordar el trabajo importante que hemos realizado durante una jornada para
completar la informaci&oacute;n que pueda hacer falta para un d&iacute;a determinado. </p>
</div>
